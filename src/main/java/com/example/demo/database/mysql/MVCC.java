package com.example.demo.database.mysql;

/**
 * MVCC多版本并发控制
 *
 * 【核心思想】
 * 通过维护数据的历史版本，实现非阻塞读。
 * 读操作读取历史版本，写操作创建新版本，读写互不阻塞。
 *
 * 【解决的问题】
 * 1. 读操作不阻塞写操作
 * 2. 写操作不阻塞读操作
 * 3. 解决读写冲突，提高并发性能
 *
 * 【MySQL InnoDB实现】
 * 1. 隐藏字段：DB_TRX_ID, DB_ROLL_PTR, DB_ROW_ID
 * 2. Undo Log：存储数据历史版本
 * 3. Read View：定义可见性规则
 */
public class MVCC {

    // ==================== 隐藏字段 ====================

    /**
     * InnoDB每行数据包含隐藏字段：
     *
     * ┌──────────────────┬─────────────────────────────────────────┐
     * │ 字段              │ 说明                                     │
     * ├──────────────────┼─────────────────────────────────────────┤
     * │ DB_TRX_ID        │ 最后插入/更新该行的事务ID（6字节）        │
     * │ DB_ROLL_PTR      │ 回滚指针，指向Undo Log中的上一版本（7字节）│
     * │ DB_ROW_ID        │ 行ID，自增（如果表没有主键时使用）        │
     * └──────────────────┴─────────────────────────────────────────┘
     *
     * 数据行结构示例：
     * ┌────────────────────────────────────────────────────────────┐
     * │ id | name | age | DB_TRX_ID | DB_ROLL_PTR | DB_ROW_ID     │
     * │ 1  | 张三 | 25  | 100       | 0x7F...     | (隐藏)        │
     * └────────────────────────────────────────────────────────────┘
     */

    // ==================== Undo Log ====================

    /**
     * Undo Log（回滚日志）
     *
     * 作用：
     * 1. 事务回滚：恢复数据到修改前的状态
     * 2. MVCC：提供数据的历史版本
     *
     * 数据版本链示例：
     *
     * ┌─────────────────────────────────────────────────────────────┐
     * │                      当前数据                                │
     * │  id=1, name='王五', DB_TRX_ID=102, DB_ROLL_PTR →            │
     * └───────────────────────────┬─────────────────────────────────┘
     *                             │
     *                             ▼
     * ┌─────────────────────────────────────────────────────────────┐
     * │                     Undo Log版本1                           │
     * │  id=1, name='李四', DB_TRX_ID=101, DB_ROLL_PTR →            │
     * └───────────────────────────┬─────────────────────────────────┘
     *                             │
     *                             ▼
     * ┌─────────────────────────────────────────────────────────────┐
     * │                     Undo Log版本2                           │
     * │  id=1, name='张三', DB_TRX_ID=100, DB_ROLL_PTR=NULL         │
     * └─────────────────────────────────────────────────────────────┘
     *
     * 遍历版本链：从最新版本开始，通过DB_ROLL_PTR依次找到历史版本
     */

    // ==================== Read View ====================

    /**
     * Read View（读视图）
     *
     * 在快照读（普通SELECT）时生成，定义哪些版本对当前事务可见。
     *
     * Read View包含的字段：
     * ┌──────────────────┬─────────────────────────────────────────┐
     * │ 字段              │ 说明                                     │
     * ├──────────────────┼─────────────────────────────────────────┤
     * │ m_ids            │ 生成Read View时，活跃的事务ID列表         │
     * │ min_trx_id       │ 活跃事务中最小的事务ID                    │
     * │ max_trx_id       │ 生成Read View时，系统应分配的下一个事务ID   │
     * │ creator_trx_id   │ 生成该Read View的事务ID                   │
     * └──────────────────┴─────────────────────────────────────────┘
     */

    /**
     * 可见性判断规则：
     *
     * 被访问版本的DB_TRX_ID与Read View的关系：
     *
     * 1. DB_TRX_ID == creator_trx_id
     *    → 自己修改的，可见
     *
     * 2. DB_TRX_ID < min_trx_id
     *    → 事务已提交，可见
     *
     * 3. DB_TRX_ID >= max_trx_id
     *    → 事务在Read View之后开启，不可见
     *
     * 4. min_trx_id <= DB_TRX_ID < max_trx_id
     *    → 需要判断是否在m_ids中
     *    - 在m_ids中：事务未提交，不可见
     *    - 不在m_ids中：事务已提交，可见
     *
     * 示例流程：
     *
     * 事务A (trx_id=100)          事务B (trx_id=101)          事务C (trx_id=102)
     *      │                            │                            │
     *      │ BEGIN                      │                            │
     *      │                            │ BEGIN                      │
     *      │ UPDATE user SET name='李四'│                            │
     *      │                            │                            │ BEGIN
     *      │ COMMIT                     │ SELECT * FROM user         │
     *      │                            │ (Read View: m_ids=[101,102])│
     *      │                            │ → 读取trx_id=100的版本      │
     *      │                            │ COMMIT                     │
     */

    // ==================== 快照读 vs 当前读 ====================

    /**
     * 快照读（Snapshot Read）
     *
     * 普通的SELECT语句，读取历史版本。
     * 不加锁，并发性能好。
     *
     * SELECT * FROM user WHERE id = 1;
     */

    /**
     * 当前读（Current Read）
     *
     * 读取最新版本，并加锁。
     *
     * -- 共享锁（S锁）
     * SELECT * FROM user WHERE id = 1 LOCK IN SHARE MODE;
     * SELECT * FROM user WHERE id = 1 FOR SHARE;  -- MySQL 8.0+
     *
     * -- 排他锁（X锁）
     * SELECT * FROM user WHERE id = 1 FOR UPDATE;
     *
     * -- 以下操作也是当前读
     * UPDATE user SET name = '王五' WHERE id = 1;
     * DELETE FROM user WHERE id = 1;
     * INSERT INTO user VALUES (1, '赵六');
     */

    // ==================== RR隔离级别的实现 ====================

    /**
     * Repeatable Read（可重复读）
     *
     * 实现方式：
     * 1. 第一次SELECT生成Read View
     * 2. 后续SELECT复用同一个Read View
     * 3. 因此看到的是事务开始时的数据快照
     *
     * 示例：
     *
     * 时刻    事务A                        事务B
     * ─────────────────────────────────────────────────────
     * T1     BEGIN
     * T2                                  BEGIN
     * T3     SELECT * FROM user
     *        (Read View生成，此时没有未提交事务)
     * T4                                  UPDATE user SET name='李四'
     * T5     SELECT * FROM user
     *        (使用之前的Read View，读取旧版本)
     * T6                                  COMMIT
     * T7     SELECT * FROM user
     *        (仍然使用之前的Read View，读取旧版本)
     * T8     COMMIT
     *
     * 结果：事务A三次SELECT结果一致（可重复读）
     */

    // ==================== RC隔离级别的实现 ====================

    /**
     * Read Committed（读已提交）
     *
     * 实现方式：
     * 1. 每次SELECT都生成新的Read View
     * 2. 因此能看到已提交的最新数据
     *
     * 示例：
     *
     * 时刻    事务A                        事务B
     * ─────────────────────────────────────────────────────
     * T1     BEGIN
     * T2                                  BEGIN
     * T3     SELECT * FROM user
     *        (Read View生成)
     * T4                                  UPDATE user SET name='李四'
     * T5     SELECT * FROM user
     *        (新的Read View，仍读旧数据，因为B未提交)
     * T6                                  COMMIT
     * T7     SELECT * FROM user
     *        (新的Read View，读到新数据)
     * T8     COMMIT
     *
     * 结果：T5和T7读取结果不同（不可重复读）
     */

    // ==================== MVCC解决幻读？ ====================

    /**
     * MySQL如何解决幻读？
     *
     * MVCC只能解决快照读的幻读问题。
     * 当前读的幻读需要通过Next-Key Lock解决。
     *
     * 快照读场景：
     * 事务A第一次SELECT生成Read View，后续SELECT复用，
     * 即使事务B插入了新数据，事务A也看不到，不会幻读。
     *
     * 当前读场景：
     * SELECT ... FOR UPDATE会加Next-Key Lock，
     * 阻止其他事务插入新数据，防止幻读。
     *
     * 示例：
     *
     * 事务A                              事务B
     * ─────────────────────────────────────────────────────────────
     * BEGIN
     * SELECT * FROM user WHERE id > 5
     * FOR UPDATE (加Next-Key Lock)
     *                                     BEGIN
     *                                     INSERT INTO user VALUES(6,...)
     *                                     → 被阻塞，无法插入
     * COMMIT
     *                                     → 插入成功
     */

    // ==================== 总结 ====================

    /**
     * MVCC核心机制总结：
     *
     * ┌─────────────────────────────────────────────────────────────┐
     * │                     MVCC工作流程                             │
     * ├─────────────────────────────────────────────────────────────┤
     * │ 1. 写操作                                                    │
     * │    - 修改数据时，将旧版本写入Undo Log                         │
     * │    - 更新DB_TRX_ID和DB_ROLL_PTR                              │
     * │    - 形成版本链                                               │
     * ├─────────────────────────────────────────────────────────────┤
     * │ 2. 读操作（快照读）                                           │
     * │    - 生成/复用Read View                                       │
     * │    - 根据可见性规则判断版本是否可见                            │
     * │    - 不可见则通过版本链找下一个版本                            │
     * │    - 直到找到可见版本或版本链结束                              │
     * ├─────────────────────────────────────────────────────────────┤
     * │ 3. 读操作（当前读）                                           │
     * │    - 直接读取最新版本                                         │
     * │    - 加锁保护                                                 │
     * └─────────────────────────────────────────────────────────────┘
     *
     * MVCC优势：
     * 1. 读写不冲突，提高并发性能
     * 2. 快照读不需要加锁
     * 3. 实现了RR和RC隔离级别
     *
     * MVCC劣势：
     * 1. 需要维护历史版本，占用存储空间
     * 2. 长事务可能导致Undo Log膨胀
     * 3. 版本链过长时，查询性能下降
     */
}
