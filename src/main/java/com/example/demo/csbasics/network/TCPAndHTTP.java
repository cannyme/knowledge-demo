package com.example.demo.csbasics.network;

/**
 * TCP/IP与HTTP协议
 *
 * 【网络分层模型】
 * ┌─────────────────────────────────────────────────────────────┐
 * │                    OSI七层模型                               │
 * ├─────────────────┬─────────────────┬─────────────────────────┤
 * │ 应用层          │ HTTP, FTP, SMTP │ 数据                    │
 * │ 表示层          │ SSL/TLS         │                         │
 * │ 会话层          │ RPC             │                         │
 * ├─────────────────┼─────────────────┼─────────────────────────┤
 * │ 传输层          │ TCP, UDP        │ 段(Segment)             │
 * ├─────────────────┼─────────────────┼─────────────────────────┤
 * │ 网络层          │ IP, ICMP        │ 包(Packet)              │
 * ├─────────────────┼─────────────────┼─────────────────────────┤
 * │ 数据链路层      │ Ethernet, WiFi  │ 帧(Frame)               │
    │ │ 物理层          │ 光纤, 网线      │ 比特(Bit)               │
 * └─────────────────┴─────────────────┴─────────────────────────┘
 */
public class TCPAndHTTP {

    // ==================== TCP三次握手 ====================

    /**
     * TCP三次握手建立连接：
     *
     *     Client                                    Server
     *        │                                        │
     *        │ ──────── SYN=1, seq=x ────────────────→│
     *        │         (请求建立连接)                   │
     *        │                                        │
     *        │ ←─────── SYN=1, ACK=1, seq=y, ack=x+1 ─│
     *        │         (确认并同意建立连接)             │
     *        │                                        │
     *        │ ──────── ACK=1, seq=x+1, ack=y+1 ──────→│
     *        │         (确认服务端的确认)               │
     *        │                                        │
     *        │         连接建立成功                     │
     *
     * 关键字段：
     * - SYN (Synchronize)：请求建立连接
     * - ACK (Acknowledge)：确认
     * - seq (Sequence Number)：序列号
     * - ack (Acknowledge Number)：确认号
     *
     * 为什么是三次握手？
     * - 防止已失效的连接请求突然到达服务端
     * - 假设两次握手：客户端发送的SYN在网络中滞留，
     *   客户端超时重发，建立连接后，滞留的SYN到达，
     *   服务端误认为是新连接，浪费资源
     */

    // ==================== TCP四次挥手 ====================

    /**
     * TCP四次挥手断开连接：
     *
     *     Client                                    Server
     *        │                                        │
     *        │ ──────── FIN=1, seq=u ─────────────────→│
     *        │         (请求关闭连接)                   │
     *        │                                        │
     *        │ ←─────── ACK=1, seq=v, ack=u+1 ─────────│
     *        │         (确认关闭请求)                   │
     *        │                                        │
     *        │           服务端可能还有数据要发送        │
     *        │                                        │
     *        │ ←─────── FIN=1, ACK=1, seq=w, ack=u+1 ──│
     *        │         (服务端请求关闭)                 │
     *        │                                        │
     *        │ ──────── ACK=1, seq=u+1, ack=w+1 ──────→│
     *        │         (确认服务端关闭)                 │
     *        │                                        │
     *        │         等待2MSL后关闭                   │
     *
     * 为什么是四次挥手？
     * - TCP是全双工，每个方向需要单独关闭
     * - 服务端收到FIN后，可能还有数据要发送
     *
     * 为什么客户端要等待2MSL？
     * - MSL (Maximum Segment Lifetime)：报文最大生存时间
     * - 确保最后一个ACK能到达服务端
     * - 等待网络中旧报文消失
     *
     * 状态变化：
     * Client: ESTABLISHED → FIN_WAIT_1 → FIN_WAIT_2 → TIME_WAIT → CLOSED
     * Server: ESTABLISHED → CLOSE_WAIT → LAST_ACK → CLOSED
     */

    // ==================== TCP vs UDP ====================

    /**
     * ┌─────────────────┬─────────────────────┬─────────────────────┐
     * │ 特性             │ TCP                 │ UDP                 │
     * ├─────────────────┼─────────────────────┼─────────────────────┤
     * │ 连接             │ 面向连接             │ 无连接              │
     * │ 可靠性           │ 可靠（确认、重传）    │ 不可靠              │
     * │ 顺序             │ 有序                 │ 无序                │
     * │ 流量控制         │ 有（滑动窗口）        │ 无                  │
     * │ 拥塞控制         │ 有                   │ 无                  │
     * │ 传输效率         │ 较低                 │ 较高                │
     * │ 应用场景         │ 文件传输、HTTP       │ 视频、音频、DNS     │
     * └─────────────────┴─────────────────────┴─────────────────────┘
     */

    // ==================== TCP可靠传输机制 ====================

    /**
     * 1. 序列号与确认应答
     *    - 每个字节都有序号
     *    - 接收方返回确认号，表示期望收到的下一个序号
     *
     * 2. 超时重传
     *    - 发送数据后启动定时器
     *    - 超时未收到ACK则重传
     *    - RTO (Retransmission Timeout) 动态计算
     *
     * 3. 滑动窗口
     *    - 发送窗口：已发送但未收到确认的数据
     *    - 接收窗口：接收方缓冲区大小
     *    - 流量控制：发送方根据接收窗口调整发送速率
     *
     * 4. 拥塞控制
     *    - 慢开始：指数增长
     *    - 拥塞避免：线性增长
     *    - 快重传：收到3个重复ACK立即重传
     *    - 快恢复：拥塞窗口减半
     */

    // ==================== HTTP协议 ====================

    /**
     * HTTP请求报文格式：
     *
     * ┌─────────────────────────────────────────────────────────────┐
     * │ POST /api/user HTTP/1.1              ← 请求行               │
     * │ Host: www.example.com                                        │
     * │ Content-Type: application/json        ← 请求头             │
     * │ Authorization: Bearer xxx                                    │
     * │                                                              │
     * │ {"name":"张三","age":25}             ← 请求体               │
     * └─────────────────────────────────────────────────────────────┘
     *
     * HTTP响应报文格式：
     *
     * ┌─────────────────────────────────────────────────────────────┐
     * │ HTTP/1.1 200 OK                       ← 状态行              │
     * │ Content-Type: application/json        ← 响应头              │
     * │ Content-Length: 27                                           │
     * │                                                              │
     * │ {"code":0,"message":"success"}        ← 响应体              │
     * └─────────────────────────────────────────────────────────────┘
     */

    // ==================== HTTP方法 ====================

    /**
     * ┌────────┬─────────────────┬─────────────────┬─────────────────┐
     * │ 方法   │ 作用             │ 是否安全         │ 是否幂等         │
     * ├────────┼─────────────────┼─────────────────┼─────────────────┤
     * │ GET    │ 获取资源         │ 是              │ 是              │
     * │ POST   │ 创建资源         │ 否              │ 否              │
     * │ PUT    │ 更新资源（全量）  │ 否              │ 是              │
     * │ PATCH  │ 更新资源（部分）  │ 否              │ 是              │
     * │ DELETE │ 删除资源         │ 否              │ 是              │
     * │ HEAD   │ 获取响应头       │ 是              │ 是              │
     * │ OPTIONS│ 获取支持的方法    │ 是              │ 是              │
     * └────────┴─────────────────┴─────────────────┴─────────────────┘
     *
     * 安全：不修改服务器资源
     * 幂等：多次执行结果相同
     */

    // ==================== HTTP状态码 ====================

    /**
     * ┌────────┬─────────────────────────────────────────────────────┐
     * │ 状态码  │ 说明                                                 │
     * ├────────┼─────────────────────────────────────────────────────┤
     * │ 1xx    │ 信息响应                                             │
     * │ 100    │ Continue 继续发送请求体                               │
     * │ 101    │ Switching Protocols 切换协议（WebSocket）            │
     * ├────────┼─────────────────────────────────────────────────────┤
     * │ 2xx    │ 成功                                                 │
     * │ 200    │ OK 请求成功                                          │
     * │ 201    │ Created 创建成功                                     │
     * │ 204    │ No Content 成功但无响应体                             │
     * │ 206    │ Partial Content 范围请求成功                          │
     * ├────────┼─────────────────────────────────────────────────────┤
     * │ 3xx    │ 重定向                                               │
     * │ 301    │ Moved Permanently 永久重定向                          │
     * │ 302    │ Found 临时重定向                                      │
     * │ 304    │ Not Modified 缓存有效                                 │
     * │ 307    │ Temporary Redirect 临时重定向（保持方法）              │
     * │ 308    │ Permanent Redirect 永久重定向（保持方法）              │
     * ├────────┼─────────────────────────────────────────────────────┤
     * │ 4xx    │ 客户端错误                                           │
     * │ 400    │ Bad Request 请求格式错误                              │
     * │ 401    │ Unauthorized 未认证                                   │
     * │ 403    │ Forbidden 无权限                                     │
     * │ 404    │ Not Found 资源不存在                                  │
     * │ 405    │ Method Not Allowed 方法不允许                         │
     * │ 408    │ Request Timeout 请求超时                              │
     * │ 429    │ Too Many Requests 请求过多                            │
     * ├────────┼─────────────────────────────────────────────────────┤
     * │ 5xx    │ 服务端错误                                           │
     * │ 500    │ Internal Server Error 服务器内部错误                  │
     * │ 502    │ Bad Gateway 网关错误                                  │
     * │ 503    │ Service Unavailable 服务不可用                        │
     * │ 504    │ Gateway Timeout 网关超时                              │
     * └────────┴─────────────────────────────────────────────────────┘
     */

    // ==================== HTTP版本对比 ====================

    /**
     * ┌─────────────────┬─────────────────────┬─────────────────────┬─────────────────────┐
     * │ 特性             │ HTTP/1.0            │ HTTP/1.1            │ HTTP/2.0            │
     * ├─────────────────┼─────────────────────┼─────────────────────┼─────────────────────┤
     * │ 连接             │ 短连接              │ 长连接（默认）       │ 多路复用             │
     * │ 请求方式         │ 串行               │ 管道化（不实用）     │ 并行（二进制帧）      │
     * │ 头部压缩         │ 无                  │ 无                  │ HPACK压缩           │
     * │ 服务器推送        │ 不支持              │ 不支持              │ 支持                 │
     * │ 数据格式         │ 文本               │ 文本                │ 二进制               │
     * └─────────────────┴─────────────────────┴─────────────────────┴─────────────────────┘
     *
     * HTTP/1.1 队头阻塞问题：
     * - 前一个请求未完成，后一个请求必须等待
     * - 管道化不实用，浏览器默认关闭
     *
     * HTTP/2.0 解决方案：
     * - 多路复用：一个TCP连接并行多个请求
     * - 二进制分帧：将请求/响应分成多个帧
     * - 头部压缩：减少重复头部传输
     * - 服务器推送：主动推送资源
     *
     * HTTP/3.0 (QUIC)：
     * - 基于UDP实现可靠传输
     * - 解决TCP层面的队头阻塞
     */

    // ==================== HTTPS ====================

    /**
     * HTTPS = HTTP + TLS/SSL
     *
     * TLS握手过程：
     *
     *     Client                                    Server
     *        │                                        │
     *        │ ──────── ClientHello ─────────────────→│
     *        │   (支持的加密套件、随机数)               │
     *        │                                        │
     *        │ ←─────── ServerHello ──────────────────│
     *        │   (选定的加密套件、随机数)               │
     *        │                                        │
     *        │ ←─────── Certificate ──────────────────│
     *        │   (服务器证书)                          │
     *        │                                        │
     *        │ ←─────── ServerHelloDone ──────────────│
     *        │                                        │
     *        │ ──────── ClientKeyExchange ───────────→│
     *        │   (加密的预主密钥)                      │
     *        │                                        │
     *        │ ──────── ChangeCipherSpec ────────────→│
     *        │ ──────── Finished ────────────────────→│
     *        │                                        │
     *        │ ←─────── ChangeCipherSpec ─────────────│
     *        │ ←─────── Finished ─────────────────────│
     *        │                                        │
     *        │         开始加密通信                     │
     *
     * 密钥交换：
     * 1. 客户端生成随机预主密钥(Pre-Master Secret)
     * 2. 用服务器公钥加密发送
     * 3. 服务器私钥解密获得预主密钥
     * 4. 双方用预主密钥+两个随机数生成会话密钥
     *
     * 对称加密：AES（通信数据加密）
     * 非对称加密：RSA/ECC（密钥交换）
     * 哈希算法：SHA256（完整性校验）
     */

    // ==================== Cookie与Session ====================

    /**
     * Cookie：
     * - 存储在客户端
     * - 每次请求自动携带
     * - 大小限制4KB
     * - 可设置过期时间、域、路径、HttpOnly、Secure
     *
     * Session：
     * - 存储在服务端
     * - 通过Session ID关联客户端
     * - 安全性更高
     * - 占用服务器内存
     *
     * 分布式Session问题：
     * - 多台服务器无法共享Session
     * - 解决方案：
     *   1. Session粘滞（Nginx ip_hash）
     *   2. Session复制（广播同步）
     *   3. 集中式存储（Redis）
     *   4. JWT（无状态）
     */

    // ==================== 跨域问题 ====================

    /**
     * 同源策略：协议+域名+端口必须相同
     *
     * 跨域解决方案：
     *
     * 1. CORS（Cross-Origin Resource Sharing）
     *    Access-Control-Allow-Origin: *
     *    Access-Control-Allow-Methods: GET, POST
     *    Access-Control-Allow-Headers: Content-Type
     *    Access-Control-Allow-Credentials: true
     *
     * 2. JSONP（只支持GET）
     *    利用script标签不受同源策略限制
     *
     * 3. 代理服务器
     *    Nginx反向代理
     *
     * 4. postMessage
     *    用于iframe跨域通信
     */

    // ==================== 常见面试题 ====================

    /**
     * Q1: 三次握手为什么不是两次？
     * A: 防止已失效的连接请求突然到达服务端，造成资源浪费。
     *
     * Q2: 四次挥手为什么TIME_WAIT要等2MSL？
     * A: 确保最后一个ACK能到达服务端，等待网络中旧报文消失。
     *
     * Q3: GET和POST的区别？
     * A: GET幂等、可缓存、参数在URL；POST非幂等、不可缓存、参数在Body。
     *
     * Q4: HTTPS如何保证安全？
     * A: 通过TLS协议，使用非对称加密交换密钥，对称加密通信数据。
     *
     * Q5: HTTP/2.0相比1.1有什么改进？
     * A: 多路复用、二进制分帧、头部压缩、服务器推送。
     */
}
